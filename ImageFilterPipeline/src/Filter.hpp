#pragma once
// Защита от повторного включения — этот файл будет обработан компилятором только один раз.

#include <string>  // std::string — для имени фильтра
#include "Image.hpp"

namespace ifp {

///
/// Pipeline хранит фильтры как unique_ptr<Filter> — это демонстрирует
/// полиморфизм через указатели на базовый класс.
class Filter {
 public:
  /// Виртуальный деструктор — обязателен для базовых классов с виртуальными методами.
  /// Без него при удалении объекта через указатель на базовый класс (Filter*)
  /// деструктор производного класса не вызовется → утечка ресурсов.
  virtual ~Filter() = default;

  /// Чисто виртуальный метод (= 0) — каждый производный класс ОБЯЗАН его реализовать.
  /// Принимает Image по ссылке и модифицирует его пиксели на месте.
  virtual void apply(Image& img) const = 0;

  /// Возвращает читаемое имя фильтра — для логов и отображения в меню.
  /// [[nodiscard]] — компилятор предупредит, если результат игнорируется.
  [[nodiscard]] virtual std::string name() const = 0;

  // Запрет копирования — фильтры хранятся в Pipeline через unique_ptr,
  // поэтому копирование не нужно и может привести к ошибкам.
  // = delete явно запрещает компилятору генерировать эти методы.
  Filter(const Filter&)            = delete;
  Filter& operator=(const Filter&) = delete;

 protected:
  /// Конструктор защищён — нельзя создать объект абстрактного Filter напрямую,
  /// только через производные классы (GrayscaleFilter, BlurFilter и т.д.).
  Filter() = default;

  /// Вспомогательный метод: ограничивает float-значение диапазоном [0, 255]
  /// и приводит к uint8_t. Используется во всех фильтрах при записи пикселей.
  ///
  /// static — не требует объекта, вызывается как Filter::clamp(v)
  /// constexpr — вычисляется на этапе компиляции, если аргумент известен заранее
  /// noexcept — не бросает исключений
  [[nodiscard]] static constexpr uint8_t clamp(float v) noexcept {
    if (v < 0.0f)   return 0;    // значения меньше 0 → чёрный
    if (v > 255.0f) return 255;  // значения больше 255 → белый
    return static_cast<uint8_t>(v);
  }
};

}  // namespace ifp